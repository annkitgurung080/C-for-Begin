#include<stdio.h>

int main()
{

    int a = 100;  // %d or %i
    printf("%d \n",a);  // %d format specifier for Integer number and \n newline

    float pi=3.14; // %f
    printf("%f \n",pi);

    char c='A'; //%c
    printf("%c \n",c);

    char nam[]="sunil kapali"; //%s
    printf("%s\n",nam);

return 0;
}